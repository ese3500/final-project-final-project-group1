{
}

void LCD_Writ_Bus(char VH,char VL, byte mode)
{   
	PORTA = VH;
	PORTC = VL;
	pulse_low(P_WR, B_WR);
}

void LCD_Write_Bus_8(char VL)
{
	PORTA = VL;
	pulse_low(P_WR, B_WR);
}

void _set_direction_registers(byte mode)
{
	DDRA = 0xFF;
	if (mode==16)
		DDRC = 0xFF;
}

void _fast_fill_16(int ch, int cl, long pix)
{
	long blocks;

	PORTA = ch;
	PORTC = cl;

	blocks = pix/16;
	for (int i=0; i<blocks; i++)
	{
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
		pulse_low(P_WR, B_WR);
	}
	if ((pix % 16) != 0)
		for (int i=0; i<(pix % 16)+1; i++)
		{
			pulse_low(P_WR, B_WR);
		}
}

